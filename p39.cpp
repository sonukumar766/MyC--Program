// constants, Manipulators and operator precedence in c++ program
#include<iostream>
using namespace std;
int main(){

    int a=89;
    cout<<"The value of a was: "<<a<<endl;

    a=90;
    
    cout<<"The value of a is:"<<a<<endl;

    //constant in c++
    const int  b=90; // it cannot be changed because it is the constant 
    cout<<"The value of b is "<<b<<endl;
    // b=67; this will give an error because b is constant

    

    return 0;

}