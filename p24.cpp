// c++ operators
#include<iostream>
using namespace std;
int main(void){
    float var1=34.4f;
    long double var2=34.4l;

    cout<<"the value of float variable is "<<var1<<endl;
    cout<<"the value of long double variable is "<<var2<<endl;
    cout<<"size of 34.4f is "<<sizeof(var1)<<endl;
    cout<<"size of 34.4l is "<<sizeof(var2)<<endl;
    

}